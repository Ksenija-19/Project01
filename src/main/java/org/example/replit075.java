package org.example;

public class replit075 {
    public static void main(String[] args) {
        int [] numbers = {45, 78, 12,  67, 55, 89, 23, 77, 88};
        int startIndex = 1;
        int increment = 3;
        for(int i = startIndex; i < numbers.length; i += increment) {
            System.out.print(numbers[i] + " ");
        }

    }

}















